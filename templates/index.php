<div class="page-header">
    <h1>What is flexbox?</h1>
</div>
<p>CSS 2.1 defined four layout modes
	<ul>
	    <li><strong>Block layout</strong> for laying out documents</li>
	    <li><strong>Inline layout</strong> for laying out text</li>
	    <li><strong>Table layout</strong> for laying out data in a tabular format</li>
	    <li><strong>Positioned layout</strong> for explicit positioning</li>
	</ul>
</p>
<p><strong>Flex layout</strong> is designed for laying out more complex applications.</p>
<p>The main purpose of a flex container is to expand items to fill available free space, and conversely shrink them to prevent overflow.</p>

